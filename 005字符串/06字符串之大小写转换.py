myStr = 'hello world and itcast and itheima and Python'

# 1.capitalize()  字符串首字母大写
capStr = myStr.capitalize()
print(capStr)

# 2.title()   字符串中每个单词首字母大写
titleStr = myStr.title()
print(titleStr)

# 3.upper()  所有字母全部转大写
upperStr = myStr.upper()
print(upperStr)

# 4.lower()  所有字母全部转小写
lowerStr = upperStr.lower()
print(lowerStr)