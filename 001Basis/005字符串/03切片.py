"""
语法：
    序列[开始位置下标:结束位置下标:步长]
"""
str1 = '0123456789'
print(str1[1:8:2])#2表示选取间隔

print(str1[1:8])#1234567
print(str1[:6])#默认从0开始
print(str1[2:])#默认到最后
print('----------')

#倒叙（间隔为负数）
print(str1[::-1])#倒叙

print(str1[-4:-1]) #下表-1表示最后一个数，一次向前类推

print(str1[-1:-4:-1])  #下标方向，与选取方向一致，才能选取出来数据