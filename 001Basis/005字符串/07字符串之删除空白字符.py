myStr = '   hello world and itcast and itheima and Python   '
print(myStr)

# 1.lstrip()  删除左侧空白字符
lstripStr = myStr.lstrip()
print(lstripStr)

# 2.rstrip()  删除右侧空白字符
rstripStr = myStr.rstrip()
print(rstripStr)

# 3.strip()  删除两侧空白字符
stripStr = myStr.strip()
print(stripStr)


